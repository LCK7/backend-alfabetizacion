module.exports = (requiredRole) => {
  return (req, res, next) => {
    if (!req.user) {
      return res.status(401).json({ msg: "Token requerido" });
    }

    if (req.user.role !== requiredRole) {
      return res
        .status(403)
        .json({ msg: `Se requiere rol ${requiredRole}` });
    }

    next();
  };
};
